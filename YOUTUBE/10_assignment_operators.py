a = 10
a += 5  # a = a + 5 = 15
print(a)
a -= 5  # a = a - 5 = 10
print(a)
a *= 5  # a = a * 5 = 50
print(a)
a /= 5  # a = a / 5 = 10
print(a)
a //= 5  # a = a // 5 = 2
print(a)
a **= 5  # a = a ** 5 = 32
print(a)
a %= 5   # a = a % 5 = 2
print(a)
